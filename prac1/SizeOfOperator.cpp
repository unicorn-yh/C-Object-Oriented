#include <bits/stdc++.h>
using namespace std;
int main() 
{
    cout<<"bool:"<<sizeof(bool)<<endl; //1
    cout<<"char:"<<sizeof(char)<<endl; //1
    cout<<"int:"<<sizeof(int)<<endl;   //4
    cout<<"float:"<<sizeof(float)<<endl; //4
    cout<<"double:"<<sizeof(double)<<endl; //8
    cout<<"long double:"<<sizeof(long double)<<endl; //8
}